#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - add 2 integers
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - add 2 integers
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - add 2 integers
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - add 2 integers
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - add 2 integers
 * @a: parameter
 * @b: parameter
 * Return: int
 */
int op_mod(int a, int b)
{
return (a % b);
}


